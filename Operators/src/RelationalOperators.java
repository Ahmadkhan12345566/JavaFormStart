
public class RelationalOperators {

	
	
public boolean equal(int num1,int num2 ){
		
		if(num1==num2){
			return true;
		}
		
		return false;
		
	}
	
	
public boolean notequal(int num1,int num2 ){
		
		if(num1!=num2){
			return true;
		}
		
		return false;
		
	}	

public boolean greater(int num1,int num2 ){
	
	if(num1>num2){
		return true;
	}
	
	return false;
	
}


public boolean less(int num1,int num2 ){
	
	if(num1<num2){
		return true;
	}
	
	return false;
	
}



	
	
	
}


